SELECT "asdasdasd",
       "video_end_cnt",
       round("video_jump_cnt"/CAST("asdasdasd" AS DOUBLE), 2) as "jump_rate"
FROM "asdasd"."asdasdasd"
WHERE 1=1
  AND "asdasdasd" IS NOT NULL
LIMIT 5;
